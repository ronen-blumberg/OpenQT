static unsigned short rd16(const unsigned char *p)
{
    return (unsigned short)p[0] | ((unsigned short)p[1] << 8);
}

static unsigned long rd32(const unsigned char *p)
{
    return (unsigned long)p[0]
         | ((unsigned long)p[1] << 8)
         | ((unsigned long)p[2] << 16)
         | ((unsigned long)p[3] << 24);
}

static int read_at(FILE *fp, long off, void *dst, size_t n)
{
    if (fseek(fp, off, SEEK_SET) != 0) return 0;
    return fread(dst, 1, n, fp) == n;
}

/* Locate the cpih_offset for a given codepage id. Returns 0 on miss. */
static long find_codepage(FILE *fp, int want_cp)
{
    unsigned char hdr[24];
    unsigned char cpeh[40];
    long fih_off, p;
    int num_cps, i;

    if (!read_at(fp, 0, hdr, 24)) return 0;
    if (hdr[0] != 0xFF || memcmp(hdr+1, "FONT   ", 7) != 0) {
        fprintf(stderr, "Not a FONT-format CPI file\n");
        return 0;
    }
    fih_off = (long)rd32(hdr + 0x13);

    if (!read_at(fp, fih_off, hdr, 2)) return 0;
    num_cps = rd16(hdr);

    p = fih_off + 2;
    for (i = 0; i < num_cps; i++) {
        if (!read_at(fp, p, cpeh, 28)) return 0;
        if ((int)rd16(cpeh + 0x10) == want_cp) {
            return (long)rd32(cpeh + 0x18);
        }
        p = (long)rd32(cpeh + 0x02);
        if (p == 0) break;
    }
    return 0;
}

/* Within a CPIH, find the 8x16 font and copy its raster into fontbuf. */
static int load_8x16(FILE *fp, long cpih_off)
{
    unsigned char hdr[6];
    unsigned char fhdr[6];
    int num_fonts, i;
    long p;
    int height, width, numch;

    if (!read_at(fp, cpih_off, hdr, 6)) return 0;
    num_fonts = rd16(hdr + 2);
    p = cpih_off + 6;

    for (i = 0; i < num_fonts; i++) {
        if (!read_at(fp, p, fhdr, 6)) return 0;
        height = fhdr[0];
        width  = fhdr[1];
        numch  = rd16(fhdr + 4);
        p += 6;

        if (height == 16 && width == 8 && numch == 256) {
            if (!read_at(fp, p, fontbuf, FONT_BYTES)) return 0;
            return 1;
        }
        p += (long)height * numch;
    }
    fprintf(stderr, "No 8x16 font found in this codepage\n");
    return 0;
}

/* INT 10h AX=1110h: upload user font to VGA char generator.
 * first_char = starting code point (DX register).
 * count      = number of code points to upload (CX register).
 * The font data pointed to by ES:BP must begin with the glyph for
 * `first_char`, so we offset into fontbuf[] accordingly.
 *
 * Uses REGPACK rather than `union REGS`/`int86x` because Open Watcom's
 * WORDREGS does not expose BP — and BP is required for this BIOS call
 * (it's the offset half of the ES:BP font-data pointer). */
static void upload_to_vga_range(int first_char, int count)
{
    union REGPACK r;

    memset(&r, 0, sizeof(r));
    r.h.ah = 0x11;
    r.h.al = 0x10;
    r.h.bh = FONT_HEIGHT;
    r.h.bl = 0;
    r.w.cx = (unsigned short)count;
    r.w.dx = (unsigned short)first_char;
    r.w.es = FP_SEG(fontbuf);
    r.w.bp = (unsigned short)(FP_OFF(fontbuf) + (first_char * FONT_HEIGHT));
    intr(0x10, &r);
}

static void upload_to_vga(void)
{
    upload_to_vga_range(0, NUM_CHARS);
}

/* Partial upload: only the Arabic letter range 0xA0-0xFE (95 chars).
 * Use this when another font (e.g. CP862 Hebrew via HEBVGA) is already
 * loaded and we want to overlay just the Arabic glyphs on top without
 * disturbing 0x00-0x9F. */
static void upload_arabic_only(void)
{
    upload_to_vga_range(0xA0, 0xFF - 0xA0);  /* 0xA0..0xFE inclusive */
}

static int load_from_cpi(const char *path, int cp)
{
    FILE *fp;
    long cpih_off;

    fp = fopen(path, "rb");
    if (!fp) {
        fprintf(stderr, "ARABVGA: cannot open %s\n", path);
        return 0;
    }
    cpih_off = find_codepage(fp, cp);
    if (!cpih_off) {
        fprintf(stderr, "ARABVGA: codepage %d not found in %s\n", cp, path);
        fclose(fp);
        return 0;
    }
    if (!load_8x16(fp, cpih_off)) {
        fprintf(stderr, "ARABVGA: failed to read 8x16 font for CP %d\n", cp);
        fclose(fp);
        return 0;
    }
    fclose(fp);
    return 1;
}

int main(int argc, char **argv)
{
    int   cp   = DEFAULT_CP;
    char *path = NULL;
    int i;
    int partial = 0;

    for (i = 1; i < argc; i++) {
        char *a = argv[i];
        if ((a[0]=='/' || a[0]=='-') &&
            (a[1]=='C'||a[1]=='c') && (a[2]=='P'||a[2]=='p') && a[3]=='=') {
            cp = atoi(a + 4);
        } else if ((a[0]=='/' || a[0]=='-') &&
                   (a[1]=='F'||a[1]=='f') && a[2]=='=') {
            path = a + 3;
        } else if ((a[0]=='/' || a[0]=='-') &&
                   (a[1]=='P'||a[1]=='p') && a[2]=='\0') {
            partial = 1;
        } else if (strcmp(a,"/?")==0 || strcmp(a,"-?")==0 ||
                   strcmp(a,"-h")==0 || strcmp(a,"--help")==0) {
            printf("ARABVGA - Arabic VGA font loader for OpenQT\n");
            printf("Usage: ARABVGA              -> upload embedded CP864 (full 256)\n");
            printf("       ARABVGA /P           -> partial upload, 0xA0-0xFE only\n");
            printf("       ARABVGA /F=file.cpi /CP=nnn [/P] -> load from CPI\n");
            return 0;
        }
    }

    if (path) {
        if (!load_from_cpi(path, cp)) return 1;
        if (partial) {
            upload_arabic_only();
            printf("ARABVGA: overlaid Arabic 0xA0-0xFE from CP%d in %s\n", cp, path);
        } else {
            upload_to_vga();
            printf("ARABVGA: loaded CP%d from %s\n", cp, path);
        }
    } else {
        if (partial) {
            upload_arabic_only();
            printf("ARABVGA: overlaid embedded CP864 Arabic letters (0xA0-0xFE)\n");
        } else {
            upload_to_vga();
            printf("ARABVGA: loaded embedded CP864 Arabic font\n");
        }
    }
    return 0;
}
